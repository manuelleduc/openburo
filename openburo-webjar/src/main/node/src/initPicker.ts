import { waitForElement } from "./utils/waitForElement";

export async function initPicker() {
  /*
     wait for AddAttachment to be part of the dom, which is unknown since
     1. the attachment tab can be absent from the UI
     2. the tab is loaded asynchronously
    */
  await waitForElement("AddAttachment");
  // Load it asynchronously to present it from being loaded when AddAttachment is not part of the UI;
  (await import("./addPickerToDOM")).addPickerToDOM();
}
